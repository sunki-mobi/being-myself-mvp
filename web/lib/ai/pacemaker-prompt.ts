/**
 * 매 턴마다 컨텍스트로 들어가는 사용자 정보 헤더 + 데이터 컨텍스트.
 *
 * 본인 데이터(baseline·최근 답변·일기)는 server-side에서 user.id로 조회 후
 * 여기에 텍스트로 주입. demo 트랙은 페르소나 baselineSummary만.
 * 셋 다 비어 있으면 LLM은 일반 톤으로 fallback.
 */
export function buildContextHeader(args: {
  userName: string;
  turn: number;
  /** baseline 보고서 요약 — 본인(BaselineShape) 또는 페르소나(BaselineReport) */
  baselineSummary?: string;
  /** 최근 누적 답변 텍스트 list (Q1 깊이 자료) */
  recentAnswers?: string;
  /** 최근 소명일기 entry 텍스트 list (Q1 깊이 자료) */
  recentDiary?: string;
  mode?: "me" | "demo";
  /**
   * Q2 turn에서만 채워짐 — 풀에서 사용자가 받은 적 없는 후보 5~10개 텍스트.
   * 주어지면 LLM은 이 안에서만 1개 골라 question 필드에 넣고 id 출력.
   */
  q2Candidates?: string;
}): string {
  const head = `[현재 세션]
- 사용자 이름: ${args.userName}
- 현재 턴: ${args.turn}번째 (사용자 답변 기준)
- 모드: ${args.mode ?? "me"}`;

  const modeNote =
    args.mode === "demo"
      ? `

[demo 모드 안내]
박람회 게스트 빠른 체험. 매 turn마다 suggestedAnswers 1~2개를 채워주세요. 1개가 기본, 결이 다른 두 각도가 정말 가치 있을 때만 2개. baseline 결을 살짝 다른 각도로 보여주는 짧은 답변 후보. isComplete=true일 때는 빈 배열.`
      : `

[me 모드 안내]
사용자 본인의 자유 답변에 집중. suggestedAnswers는 항상 빈 배열.`;

  let out = head + modeNote;

  if (args.baselineSummary) {
    out += `\n\n[사용자 baseline 보고서 — Q1 깊이 들어가는 자료 (우선순위 2)]\n${args.baselineSummary}`;
  }
  if (args.recentAnswers) {
    out += `\n\n${args.recentAnswers}`;
  }
  if (args.recentDiary) {
    out += `\n\n${args.recentDiary}`;
  }

  if (!args.baselineSummary && !args.recentAnswers && !args.recentDiary) {
    out += `\n\n[컨텍스트 비어 있음] 누적 데이터 없음. Q1·Q2 모두 일반 톤(서로 다른 영역의 1단계 현상 질문 2개)으로.`;
  }

  // Q2 차례(turn=1) — 풀에서 1개 선택. q2Candidates가 있으면 풀 기반,
  // 없으면(demo 트랙 등) 기존 카테고리 분리 가이드로 fallback.
  if (args.turn === 1) {
    if (args.q2Candidates) {
      out += `

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
[★★★ 지금 Q2 차례 — 풀 후보 중 1개 선택 ★★★]
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

${args.q2Candidates}

▶ **선택 기준**:
1. 직전 사용자 답변·baseline·일기와 **결이 맞는** 1개
2. 직전 Q1과 다른 각도(영역·깊이·시간)면 더 좋음
3. 결정 못 하겠으면 list 상단(상위 점수) 쪽 우선

▶ **출력**:
- question 필드: 후보 본문을 그대로 또는 사용자 표현 한 조각만 살짝 녹여서 (전면 재작성 X)
- selectedQuestionId 필드: 선택한 id 그대로 (예: "Q017")
- reaction 필드: 직전 답변 한 조각 인용한 1문장

🚫 **금지**:
- list 밖의 질문 자유 생성 (반드시 후보 중 1개)
- 같은 turn에 후보 여러 개 합치기
- id 빈 문자열로 두기 (선택 못 한 경우만 빈 문자열, 그 외엔 반드시 채울 것)`;
    } else {
      out += `

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
[★★★ 지금 Q2 차례 — 매우 중요 ★★★]
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

위 history에서 Q1과 사용자 답변을 봤죠. 사용자 답변을 **토대로** Q2를 만들되, **Q1과 다른 카테고리**여야 합니다.

🔍 **먼저 Q1의 카테고리를 식별**하세요. 6 카테고리(경험탐색·과거성공·본질추출·패턴연결·시점이동·미래가능성) 중 어느 것인가?

✅ **그 다음 — 다른 카테고리로 이끌기**:
- depth up: Q1보다 깊은 층 (예: 경험탐색 → 본질추출/시점이동)
- 가로 이동: 같은 층 다른 카테고리 (예: 경험탐색 → 과거성공)

🚫 **절대 금지**: Q1과 같은 카테고리.

selectedQuestionId는 빈 문자열로 두세요(풀 미사용 turn).`;
    }
  } else {
    // Q1 또는 마무리 turn — selectedQuestionId는 빈 문자열
    out += `\n\n[selectedQuestionId 안내] 이번 turn은 풀 사용 turn이 아닙니다. selectedQuestionId는 빈 문자열로 두세요.`;
  }

  return out;
}
